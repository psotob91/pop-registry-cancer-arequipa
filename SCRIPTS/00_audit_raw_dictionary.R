canonical_seed <- data_dictionary_by_sheet %>%
  mutate(
    canonical_candidate = case_when(
      possible_iarc_field == "residencia" & clean_name == "res" ~ "residencia__res",
      possible_iarc_field == "residencia" & clean_name == "deptres" ~ "residencia__deptres",
      possible_iarc_field == "residencia" & clean_name == "provdist" ~ "residencia__provdist",
      possible_iarc_field == "multiple_primary" & clean_name == "pmseq" ~ "multiple_primary__pmseq",
      possible_iarc_field == "multiple_primary" & clean_name == "pmtot" ~ "multiple_primary__pmtot",
      possible_iarc_field == "multiple_primary" & clean_name == "pmcod" ~ "multiple_primary__pmcod",
      !is.na(possible_iarc_field) ~ possible_iarc_field,
      TRUE ~ clean_name
    )
  )

data_dictionary_crosswalk <- canonical_seed %>%
  group_by(canonical_candidate) %>%
  summarise(
    raw_name_variants = paste(sort(unique(raw_name)), collapse = " | "),
    clean_name_variants = paste(sort(unique(clean_name)), collapse = " | "),
    sheets_present = paste(sort(unique(sheet)), collapse = " | "),
    first_year_present = suppressWarnings(min(year_sheet, na.rm = TRUE)),
    last_year_present = suppressWarnings(max(year_sheet, na.rm = TRUE)),
    appears_all_years = n_distinct(year_sheet) == n_distinct(canonical_seed$year_sheet),
    likely_same_field_across_sheets = case_when(
      n_distinct(clean_name) == 1 ~ "yes_direct",
      n_distinct(clean_name) <= 2 ~ "probably_yes",
      TRUE ~ "uncertain"
    ),
    possible_iarc_field = {
      x <- na.omit(possible_iarc_field)
      if (length(x) == 0) NA_character_ else names(sort(table(x), decreasing = TRUE))[1]
    },
    confidence_iarc_mapping = {
      x <- na.omit(confidence_iarc_mapping)
      if (length(x) == 0) "unknown" else names(sort(table(x), decreasing = TRUE))[1]
    },
    harmonization_decision = case_when(
      !is.na(possible_iarc_field) & n_distinct(clean_name) == 1 ~ "candidate_ready_for_harmonization",
      !is.na(possible_iarc_field) & n_distinct(clean_name) > 1 ~ "candidate_needs_review_before_harmonization",
      TRUE ~ "not_iarc_or_uncertain"
    ),
    decision_status = case_when(
      harmonization_decision == "candidate_ready_for_harmonization" ~ "auto_preclassified",
      TRUE ~ "pending_review"
    ),
    notes = case_when(
      appears_all_years ~ "Campo estructuralmente estable en todos los años.",
      TRUE ~ "Campo no estable o con variantes; revisar antes de armonizar."
    ),
    .groups = "drop"
  ) %>%
  arrange(desc(appears_all_years), canonical_candidate)

write_csv(data_dictionary_crosswalk, out_crosswalk, na = "")

