data_dictionary_by_sheet <- imap_dfr(raw_sheets, function(df, sh) {
  raw_names <- names(df)
  year_val <- safe_parse_year(sh)
  
  map_dfr(seq_along(raw_names), function(i) {
    nm <- raw_names[i]
    x <- df[[i]]
    x_chr <- as.character(x)
    x_trim <- str_trim(x_chr)
    x_trim[x_trim == ""] <- NA_character_
    
    ex <- sample_examples(x_trim, n = 3)
    clean_name <- clean_name_conservative(nm)
    header_anomaly <- header_anomaly_class(nm)
    
    tibble(
      sheet = sh,
      year_sheet = year_val,
      col_position = i,
      raw_name = nm,
      clean_name = clean_name,
      raw_type_guess = guess_type_raw(x_trim),
      n_rows = nrow(df),
      n_non_missing = sum(!is.na(x_trim)),
      pct_missing = round(mean(is.na(x_trim)) * 100, 2),
      n_unique = dplyr::n_distinct(x_trim, na.rm = TRUE),
      example_value_1 = ex[1],
      example_value_2 = ex[2],
      example_value_3 = ex[3],
      min_value = safe_min_chr(x_trim),
      max_value = safe_max_chr(x_trim),
      domain_signature = domain_signature(x_trim),
      possible_meaning = case_when(
        str_detect(clean_name, "sexo|sex") ~ "Posible sexo",
        str_detect(clean_name, "^edad$|age") ~ "Posible edad",
        str_detect(clean_name, "fecdiag|diagn") ~ "Posible fecha de diagnóstico",
        str_detect(clean_name, "topo") ~ "Posible topografía ICD-O",
        str_detect(clean_name, "morf") ~ "Posible morfología ICD-O",
        str_detect(clean_name, "base") ~ "Posible base de diagnóstico",
        str_detect(clean_name, "lateral|late") ~ "Posible lateralidad",
        str_detect(clean_name, "res|deptres|provdist") ~ "Posible residencia",
        str_detect(clean_name, "estvit") ~ "Posible estado vital",
        str_detect(clean_name, "fecdef") ~ "Posible fecha de defunción",
        str_detect(clean_name, "fuc") ~ "Posible fecha de último contacto",
        TRUE ~ NA_character_
      ),
      possible_iarc_field = possible_iarc_from_name(nm),
      confidence_iarc_mapping = confidence_from_name(nm),
      header_anomaly = header_anomaly,
      notes = case_when(
        header_anomaly != "none" ~ paste0("Encabezado anómalo: ", header_anomaly, "; revisar estructura original de la hoja."),
        round(mean(is.na(x_trim)) * 100, 2) == 100 ~ "Columna completamente vacía.",
        dplyr::n_distinct(x_trim, na.rm = TRUE) <= 1 & sum(!is.na(x_trim)) > 0 ~ "Columna constante.",
        TRUE ~ NA_character_
      )
    )
  })
})

write_csv(data_dictionary_by_sheet, out_dict_sheet, na = "")

