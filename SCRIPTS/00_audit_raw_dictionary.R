safe_parse_year <- function(x) {
  y <- suppressWarnings(readr::parse_number(as.character(x)))
  ifelse(is.na(y), NA_real_, y)
}

clean_name_conservative <- function(x) {
  x %>%
    as.character() %>%
    str_trim() %>%
    str_replace_all("\\s+", "_") %>%
    str_replace_all("[°º]+", "") %>%
    str_replace_all("#", "number") %>%
    str_replace_all("[^A-Za-z0-9_]+", "_") %>%
    str_replace_all("_+", "_") %>%
    str_replace_all("^_|_$", "") %>%
    str_to_lower()
}

guess_type_raw <- function(x) {
  x_chr <- as.character(x)
  x_chr <- str_trim(x_chr)
  x_chr[x_chr == ""] <- NA_character_
  
  nonmiss <- x_chr[!is.na(x_chr)]
  if (length(nonmiss) == 0) return("all_missing")
  
  p_num <- mean(str_detect(nonmiss, "^-?\\d+(\\.\\d+)?$"))
  p_date <- mean(str_detect(nonmiss, "^(\\d{1,2}[/-]\\d{1,2}[/-]\\d{2,4}|\\d{4}[/-]\\d{1,2}[/-]\\d{1,2}|\\d{5})$"))
  p_int_like <- mean(str_detect(nonmiss, "^-?\\d+$"))
  
  if (p_date >= 0.7) return("date_like")
  if (p_int_like >= 0.9) return("integer_like")
  if (p_num >= 0.9) return("numeric_like")
  
  nunq <- dplyr::n_distinct(nonmiss)
  if (nunq <= 12) return("categorical_like")
  
  "character_like"
}

sample_examples <- function(x, n = 3) {
  vals <- x %>%
    as.character() %>%
    str_trim()
  
  vals[vals == ""] <- NA_character_
  
  out <- vals[!is.na(vals)] %>%
    unique()
  
  out <- head(out, n)
  length(out) <- n
  out
}

safe_min_chr <- function(x) {
  z <- x %>% as.character() %>% str_trim()
  z[z == ""] <- NA_character_
  z <- z[!is.na(z)]
  if (length(z) == 0) return(NA_character_)
  suppressWarnings(as.character(min(z)))
}

safe_max_chr <- function(x) {
  z <- x %>% as.character() %>% str_trim()
  z[z == ""] <- NA_character_
  z <- z[!is.na(z)]
  if (length(z) == 0) return(NA_character_)
  suppressWarnings(as.character(max(z)))
}

name_suspicious_flag <- function(raw_name) {
  nm <- as.character(raw_name)
  nm_trim <- str_trim(nm)
  
  is.na(nm) |
    nm_trim == "" |
    str_detect(nm, "^Unnamed") |
    str_detect(nm, "^\\.\\.\\.") |
    str_detect(nm, "^X\\d+$") |
    str_detect(nm, "^[0-9]+$") |
    str_detect(nm, "^__blank_col_")
}

header_anomaly_class <- function(raw_name) {
  nm <- as.character(raw_name)
  case_when(
    is.na(nm) ~ "missing_name",
    str_trim(nm) == "" ~ "blank_name",
    str_detect(nm, "^__blank_col_") ~ "blank_header_repaired",
    str_detect(nm, "^[0-9]+$") ~ "numeric_header",
    str_detect(nm, "^Unnamed") ~ "unnamed_header",
    str_detect(nm, "^X\\d+$") ~ "autogenerated_header",
    TRUE ~ "none"
  )
}

possible_iarc_from_name <- function(x) {
  nm <- clean_name_conservative(x)
  
  case_when(
    str_detect(nm, "sexo|sex") ~ "sexo",
    str_detect(nm, "^edad$|age") ~ "edad",
    str_detect(nm, "fecdiag|fecha_diag|diagn") ~ "fecha_diagnostico",
    str_detect(nm, "topo|topog|icdo_top") ~ "topografia_icdo",
    str_detect(nm, "morf|hist|morph") ~ "morfologia_icdo",
    str_detect(nm, "comport|behav") ~ "comportamiento",
    str_detect(nm, "grado|grade") ~ "grado",
    str_detect(nm, "lateral|late") ~ "lateralidad",
    str_detect(nm, "base|basis") ~ "base_diagnostico",
    str_detect(nm, "estvit|vital") ~ "estado_vital",
    str_detect(nm, "fecdef|fecha_def|death") ~ "fecha_muerte",
    str_detect(nm, "fuc|ultimo_contact|last_contact") ~ "fecha_ultimo_contacto",
    str_detect(nm, "res|resid|deptres|provdist|domic") ~ "residencia",
    str_detect(nm, "pmseq|pmtot|multiple|primar") ~ "multiple_primary",
    TRUE ~ NA_character_
  )
}

confidence_from_name <- function(x) {
  nm <- clean_name_conservative(x)
  
  case_when(
    nm %in% c("sexo", "edad", "fecdiag", "topo", "morf", "grado", "fuc", "estvit", "fecdef") ~ "high",
    str_detect(nm, "base|lateral|comport|deptres|provdist|res|pmseq|pmtot") ~ "moderate",
    TRUE ~ "low"
  )
}

classify_variable_role <- function(clean_name, raw_name = NA_character_, possible_iarc_field = NA_character_) {
  nm <- clean_name_conservative(clean_name)
  raw_nm <- clean_name_conservative(raw_name)
  
  case_when(
    !is.na(possible_iarc_field) ~ "epidemiologic_core",
    str_detect(nm, "^tumourid$|^patientid|recordid|nodoc|hc$|numpat|tumouridsourcetable|sourcerecordid") ~ "identifier",
    str_detect(nm, "updatedby|updatedate|recordstatus|checkstatus|obsoleteflag|unduplicationstatus") ~ "administrative",
    str_detect(nm, "notif|inst_number11|tipoest|prof|veri|estcas|modif|busc|caso|obs") ~ "administrative",
    str_detect(nm, "^1_ape$|^2_ape$|^1_nombre$|^2_nombre$|^3_nombre$|pais|estciv|ocu|instr|tipo") ~ "person_descriptive",
    str_detect(nm, "^cie10$|^cicn$|^estadio$|^tx$|^nx$|^mx$|^dx$|^causa$") ~ "clinical_auxiliary",
    str_detect(nm, "blank_col|^[0-9]+$") | raw_nm %in% c("blank_col_2", "673668") ~ "structural_anomaly",
    TRUE ~ "other"
  )
}

is_structural_garbage <- function(clean_name, header_anomaly, flag_all_missing) {
  clean_name %in% c("blank_col_2", "673668") |
    header_anomaly != "none" |
    flag_all_missing
}

is_candidate_for_indicator_qc <- function(canonical_candidate, possible_iarc_field, clean_name) {
  canonical_nm <- coalesce(canonical_candidate, "")
  iarc_nm <- coalesce(possible_iarc_field, "")
  clean_nm <- coalesce(clean_name, "")
  
  canonical_nm %in% c(
    "sexo", "edad", "fecha_diagnostico", "topografia_icdo", "morfologia_icdo",
    "comportamiento", "grado", "base_diagnostico", "estado_vital",
    "fecha_muerte", "fecha_ultimo_contacto", "lateralidad",
    "residencia__res", "residencia__deptres", "residencia__provdist",
    "multiple_primary__pmseq", "multiple_primary__pmtot", "multiple_primary__pmcod",
    "causa", "cie10"
  ) |
    iarc_nm %in% c(
      "sexo", "edad", "fecha_diagnostico", "topografia_icdo", "morfologia_icdo",
      "comportamiento", "grado", "base_diagnostico", "estado_vital",
      "fecha_muerte", "fecha_ultimo_contacto", "lateralidad", "residencia", "multiple_primary"
    ) |
    clean_nm %in% c("causa", "cie10")
}

domain_signature <- function(x, max_n = 20) {
  vals <- x %>%
    as.character() %>%
    str_trim()
  
  vals[vals == ""] <- NA_character_
  vals <- vals[!is.na(vals)]
  
  if (length(vals) == 0) return("all_missing")
  
  nunq <- dplyr::n_distinct(vals)
  
  if (nunq <= max_n) {
    sig <- vals %>%
      unique() %>%
      sort() %>%
      paste(collapse = " | ")
    return(sig)
  }
  
  paste0("high_cardinality_n=", nunq)
}

norm_adist <- function(a, b) {
  a <- as.character(a)
  b <- as.character(b)
  if (is.na(a) || is.na(b) || (a == "" && b == "")) return(0)
  d <- utils::adist(a, b)[1]
  m <- max(nchar(a), nchar(b))
  if (m == 0) return(0)
  as.numeric(d / m)
}

score_name_similarity <- function(a, b) {
  1 - norm_adist(clean_name_conservative(a), clean_name_conservative(b))
}

score_domain_similarity <- function(a, b) {
  if (is.na(a) || is.na(b)) return(0)
  if (a == b) return(1)
  if (str_detect(a, "^high_cardinality") && str_detect(b, "^high_cardinality")) return(0.5)
  0
}

score_position_similarity <- function(pos_a, pos_b, max_pos_diff = 10) {
  if (is.na(pos_a) || is.na(pos_b)) return(0)
  d <- abs(pos_a - pos_b)
  pmax(0, 1 - d / max_pos_diff)
}

evidence_class <- function(score) {
  case_when(
    score >= 0.85 ~ "evidence_direct",
    score >= 0.65 ~ "inference_probable",
    score >= 0.45 ~ "hypothesis_uncertain",
    TRUE ~ "weak_signal"
  )
}

confidence_class <- function(score) {
  case_when(
    score >= 0.85 ~ "high",
    score >= 0.65 ~ "moderate",
    score >= 0.45 ~ "low",
    TRUE ~ "unknown"
  )
}

