variable_quality_profile <- data_dictionary_by_sheet %>%
  mutate(
    flag_all_missing = pct_missing == 100,
    flag_near_empty = pct_missing >= 95 & pct_missing < 100,
    flag_name_suspicious = name_suspicious_flag(raw_name),
    flag_header_anomaly = header_anomaly != "none",
    flag_constant = n_unique <= 1 & n_non_missing > 0,
    flag_high_cardinality_text = raw_type_guess == "character_like" & n_unique > pmax(50, n_rows * 0.5),
    flag_domain_strange = case_when(
      raw_type_guess == "integer_like" & str_detect(raw_name, regex("sexo|sex", ignore_case = TRUE)) &
        !str_detect(paste(example_value_1, example_value_2, example_value_3), "^[12MFmfHh]") ~ TRUE,
      TRUE ~ FALSE
    )
  ) %>%
  group_by(sheet) %>%
  mutate(
    flag_duplicate_clean_name_within_sheet = duplicated(clean_name) | duplicated(clean_name, fromLast = TRUE)
  ) %>%
  ungroup() %>%
  mutate(
    variable_role = classify_variable_role(clean_name, raw_name, possible_iarc_field),
    flag_structural_garbage = is_structural_garbage(clean_name, header_anomaly, flag_all_missing),
    candidate_for_indicator_qc = is_candidate_for_indicator_qc(NA_character_, possible_iarc_field, clean_name),
    quality_issue_summary = pmap_chr(
      list(
        flag_all_missing,
        flag_near_empty,
        flag_name_suspicious,
        flag_header_anomaly,
        flag_constant,
        flag_high_cardinality_text,
        flag_domain_strange,
        flag_duplicate_clean_name_within_sheet,
        flag_structural_garbage
      ),
      function(a, b, c, d, e, f, g, h, i) {
        issues <- c()
        if (isTRUE(a)) issues <- c(issues, "all_missing")
        if (isTRUE(b)) issues <- c(issues, "near_empty")
        if (isTRUE(c)) issues <- c(issues, "name_suspicious")
        if (isTRUE(d)) issues <- c(issues, "header_anomaly")
        if (isTRUE(e)) issues <- c(issues, "constant")
        if (isTRUE(f)) issues <- c(issues, "high_cardinality_text")
        if (isTRUE(g)) issues <- c(issues, "domain_strange")
        if (isTRUE(h)) issues <- c(issues, "duplicate_clean_name_within_sheet")
        if (isTRUE(i)) issues <- c(issues, "structural_garbage")
        if (length(issues) == 0) "none" else paste(issues, collapse = "; ")
      }
    )
  ) %>%
  select(
    sheet, year_sheet, col_position, raw_name, clean_name, raw_type_guess,
    n_rows, n_non_missing, pct_missing, n_unique,
    header_anomaly, variable_role, candidate_for_indicator_qc,
    possible_iarc_field, confidence_iarc_mapping,
    starts_with("flag_"), quality_issue_summary
  )

write_csv(variable_quality_profile, out_var_quality, na = "")

