dict_pairs <- data_dictionary_by_sheet %>%
  select(
    sheet, year_sheet, col_position, raw_name, clean_name,
    raw_type_guess, domain_signature, n_unique,
    possible_iarc_field
  ) %>%
  mutate(
    clean_prefix = str_sub(clean_name, 1, 6),
    block_key = case_when(
      !is.na(possible_iarc_field) ~ paste0("iarc::", possible_iarc_field),
      TRUE ~ paste0("name::", clean_prefix, "::", raw_type_guess)
    )
  )

candidate_pairs <- dict_pairs %>%
  rename(
    sheet_a = sheet,
    year_a = year_sheet,
    pos_a = col_position,
    raw_name_a = raw_name,
    clean_name_a = clean_name,
    type_a = raw_type_guess,
    domain_a = domain_signature,
    n_unique_a = n_unique,
    iarc_a = possible_iarc_field,
    clean_prefix_a = clean_prefix,
    block_key = block_key
  ) %>%
  inner_join(
    dict_pairs %>%
      rename(
        sheet_b = sheet,
        year_b = year_sheet,
        pos_b = col_position,
        raw_name_b = raw_name,
        clean_name_b = clean_name,
        type_b = raw_type_guess,
        domain_b = domain_signature,
        n_unique_b = n_unique,
        iarc_b = possible_iarc_field,
        clean_prefix_b = clean_prefix,
        block_key = block_key
      ),
    by = "block_key"
  ) %>%
  filter(
    !(sheet_a == sheet_b & pos_a == pos_b),
    year_a <= year_b,
    !(year_a == year_b & pos_a >= pos_b)
  ) %>%
  filter(
    sheet_a != sheet_b |
      clean_name_a == clean_name_b |
      clean_prefix_a == clean_prefix_b |
      (!is.na(iarc_a) & !is.na(iarc_b) & iarc_a == iarc_b)
  )

equivalence_candidates <- candidate_pairs %>%
  mutate(
    score_raw_name = map2_dbl(raw_name_a, raw_name_b, score_name_similarity),
    score_clean_name = map2_dbl(clean_name_a, clean_name_b, score_name_similarity),
    score_type = if_else(type_a == type_b, 1, 0),
    score_domain = map2_dbl(domain_a, domain_b, score_domain_similarity),
    score_position = map2_dbl(pos_a, pos_b, score_position_similarity),
    score_same_iarc_hint = if_else(!is.na(iarc_a) & !is.na(iarc_b) & iarc_a == iarc_b, 1, 0),
    equivalence_score =
      0.30 * score_raw_name +
      0.30 * score_clean_name +
      0.15 * score_type +
      0.15 * score_domain +
      0.05 * score_position +
      0.05 * score_same_iarc_hint,
    evidence = map_chr(equivalence_score, evidence_class),
    confidence = map_chr(equivalence_score, confidence_class)
  ) %>%
  filter(equivalence_score >= 0.45) %>%
  arrange(desc(equivalence_score))

