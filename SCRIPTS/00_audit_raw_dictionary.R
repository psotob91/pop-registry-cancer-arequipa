# ------------------------------------------------------------
# 6) construcción de raw_tagged
#    Nota: NO colapsa ambigüedades.
#    Se guarda como lista con:
#    - data: bind_rows con audit names únicos
#    - column_map: mapa sheet/raw_name/clean_name/audit_name
# ------------------------------------------------------------
tagged_sheet_objects <- imap(raw_sheets, function(df, sh) {
  raw_names <- names(df)
  
  colmap <- tibble(
    sheet = sh,
    year_sheet = safe_parse_year(sh),
    col_position = seq_along(raw_names),
    raw_name = raw_names,
    clean_name = clean_name_conservative(raw_names),
    possible_iarc_field = map_chr(raw_names, ~ coalesce(possible_iarc_from_name(.x), NA_character_)),
    confidence_iarc_mapping = map_chr(raw_names, confidence_from_name)
  ) %>%
    group_by(sheet) %>%
    mutate(
      clean_name_unique_within_sheet = make.unique(clean_name, sep = "__dup"),
      audit_name = paste0(clean_name_unique_within_sheet, "__", str_pad(col_position, 2, pad = "0"))
    ) %>%
    ungroup()
  
  df2 <- df
  names(df2) <- colmap$audit_name
  
  df2 <- df2 %>%
    mutate(
      sheet_source = sh,
      year_sheet = safe_parse_year(sh),
      .before = 1
    )
  
  list(data = df2, column_map = colmap)
})

raw_tagged_data <- map(tagged_sheet_objects, "data") %>% bind_rows()
raw_tagged_colmap <- map(tagged_sheet_objects, "column_map") %>% bind_rows()

raw_tagged <- list(
  data = raw_tagged_data,
  column_map = raw_tagged_colmap,
  created_at = Sys.time(),
  source_file = path_xlsx
)

