repair_raw_colnames <- function(nms) {
  nms_chr <- as.character(nms)
  nms_chr[is.na(nms_chr)] <- ""
  nms_chr <- str_trim(nms_chr)
  
  nms_fixed <- if_else(
    nms_chr == "",
    paste0("__blank_col_", seq_along(nms_chr)),
    nms_chr
  )
  
  make.unique(nms_fixed, sep = "__dup")
}

raw_sheets <- set_names(
  map(sheets, function(sh) {
    df <- readxl::read_excel(
      path = path_xlsx,
      sheet = sh,
      col_types = "text",
      .name_repair = "minimal"
    ) %>%
      as_tibble(.name_repair = "minimal")
    
    names(df) <- repair_raw_colnames(names(df))
    df
  }),
  sheets
)

